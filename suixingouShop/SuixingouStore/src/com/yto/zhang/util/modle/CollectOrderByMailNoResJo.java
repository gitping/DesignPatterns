package com.yto.zhang.util.modle;

import java.util.Date;



/**
 * 获取代收包裹各种订单
 * 
 * @author Alex
 * @since 2014-11-25
 */

public class CollectOrderByMailNoResJo extends UUID {

	/**
	 * db 主键
	 */
	private Long id;
	/**
	 * 快递单号
	 */
	private String mailNo;

	/**
	 * 手机号码
	 */
	private String telephone;

	/**
	 * 创建时间=到货时间
	 */
	private long createTime;

	/**
	 * 订单编号=取件密码
	 */
	private String orderCode;

	/**
	 * 订单状态
	 */
	private String orderStatus;
	
	//取件时间
	private long expireExpressTime;
	
	//快递类型
	private  String expressCompany;	
	
	private Integer num;
	private Integer orderStatusProperty;
	/**退件时间*/
	private Long backDate;
	/**签收时间*/
	private Long signDate;
	/**扫描时间*/
	private Long scanDate;

	public Long getBackDate() {
		return backDate;
	}

	public void setBackDate(Long backDate) {
		this.backDate = backDate;
	}

	public Long getSignDate() {
		return signDate;
	}

	public void setSignDate(Long signDate) {
		this.signDate = signDate;
	}

	public Long getScanDate() {
		return scanDate;
	}

	public void setScanDate(Long scanDate) {
		this.scanDate = scanDate;
	}

	public Integer getOrderStatusProperty() {
		return orderStatusProperty;
	}

	public void setOrderStatusProperty(Integer orderStatusProperty) {
		this.orderStatusProperty = orderStatusProperty;
	}

	public Integer getNum() {
		return num;
	}

	public void setNum(Integer num) {
		this.num = num;
	}

	public String getExpressCompany() {
		return expressCompany;
	}

	public void setExpressCompany(String expressCompany) {
		this.expressCompany = expressCompany;
	}

	public long getExpireExpressTime() {
		return expireExpressTime;
	}

	public void setExpireExpressTime(long expireExpressTime) {
		this.expireExpressTime = expireExpressTime;
	}

	public String getMailNo() {
		return mailNo;
	}

	public void setMailNo(String mailNo) {
		this.mailNo = mailNo;
	}

	public String getTelephone() {
		return telephone;
	}

	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}

	public long getCreateTime() {
		return createTime;
	}

	public void setCreateTime(long createTime) {
		this.createTime = createTime;
	}

	public String getOrderCode() {
		return orderCode;
	}

	public void setOrderCode(String orderCode) {
		this.orderCode = orderCode;
	}

	public String getOrderStatus() {
		return orderStatus;
	}

	public void setOrderStatus(String orderStatus) {
		this.orderStatus = orderStatus;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

}
