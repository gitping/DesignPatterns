public class ExpressOrderUpdateReqJo extends UUID {

	/**
	 * 订单编号
	 */
	private Integer id;

	/**
	 * 参见ExpressOrderStatusEnum
	 */
	private Integer type;

	/**
	 * 预计取件时间
	 */
	private String actualExpressTime;

	/**
	 * 商家备注信息（不接单原因）
	 */
	private String statusDesc;

	/**
	 * 快递单号
	 */
	private String mailNo;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getActualExpressTime() {
		return actualExpressTime;
	}

	public void setActualExpressTime(String actualExpressTime) {
		this.actualExpressTime = actualExpressTime;
	}

	public String getMailNo() {
		return mailNo;
	}

	public void setMailNo(String mailNo) {
		this.mailNo = mailNo;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public String getStatusDesc() {
		return statusDesc;
	}

	public void setStatusDesc(String statusDesc) {
		this.statusDesc = statusDesc;
	}

}
