package com.yto.suixingouuser.activity.helper.model;

import java.io.Serializable;

/**
 * @author ShenHua
 * 2015年5月14日下午4:32:46
 */
public class PoiDetail implements Serializable{

	private String name;               //地址poi名称
	private double lat;                //地址经度
	private double lon;                //地址纬度
	private String address;            //地址
	private boolean draggable;         //该点是否可拖动
	private int initPosition;          //初始选择点
	private int count;				   //第几个poi点，便于可以用不同的序号标记显示
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getLat() {
		return lat;
	}
	public void setLat(double lat) {
		this.lat = lat;
	}
	public double getLon() {
		return lon;
	}
	public void setLon(double lon) {
		this.lon = lon;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public boolean isDraggable() {
		return draggable;
	}
	public void setDraggable(boolean draggable) {
		this.draggable = draggable;
	}
	public int getInitPosition() {
		return initPosition;
	}
	public void setInitPosition(int initPosition) {
		this.initPosition = initPosition;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}	
}
