package com.yto.suixingoustore.message;

public enum MsgNotifyTypeEnum {
	
	WAITING("0", "待接单"), RECEIVING("1", "已接单"), DELIVERING("2", "派送中"), USER_CANCEL("3", "用户取消"),REJECTED("4", "不接单"),HAVE_EXPRESS("5","已经取件"),
	ORDER_TYPE("0", "订单"),EXPRESS_TYPE("1","快递单");
	
	private String code;
	private String desc;
	
	private MsgNotifyTypeEnum(String code, String desc) {
		this.code = code;
		this.desc = desc;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}
	
	
	
}
