package com.yto.zhang.util.modle;



/**
 * 邀请补贴条目 总计在DTO2的T2中传
 * 
 * @author Andy Create on 2014 2014-5-19 下午5:22:06
 */
public class InviteSubsidyResJo extends Version {

	private Integer id;
	/** 时间 */
	private String data;
	/** 新增用户数量数量 */
	private Integer usersNum;
	/** 支付金额 */
	private Double paymentAmount;
	/** 状态 已结算: 0 结算中: 1 待结算: 2 */
	private int status;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public Double getPaymentAmount() {
		return paymentAmount;
	}

	public void setPaymentAmount(Double paymentAmount) {
		this.paymentAmount = paymentAmount;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public Integer getUsersNum() {
		return usersNum;
	}

	public void setUsersNum(Integer usersNum) {
		this.usersNum = usersNum;
	}

	

}
